using System;

class Pattern
{
    static void Main(string[] args)
    {  
        //ROWS
        for (int i = 1; i <= 7; i++)
        {
            //COLOUMNS
            for (int j = 1; j <= 10; j++)
            {
                Console.Write("*");
            }

            Console.WriteLine();
        }
        Console.ReadLine();
    }
}



     
